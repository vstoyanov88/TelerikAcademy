using System;

class ChooseVariables
{
    static void Main()
    {
        short firstV = -1000;
        sbyte secondV = -115;
        byte thirdV = 97;
        ushort fourthV =  52130;
        int fifthV = 4825932;
        Console.WriteLine("{0} {1} {2} {3} {4}", firstV, secondV, thirdV, fourthV, fifthV);
    }
}

